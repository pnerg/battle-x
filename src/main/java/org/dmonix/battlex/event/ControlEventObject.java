package org.dmonix.battlex.event;

import java.io.Serializable;

/**
 * An event object used with the ControlEventListener.
 * 
 * @author Peter Nerg
 * @version 1.0
 */
public final class ControlEventObject implements Serializable {

    private static final long serialVersionUID = 7526476784622776147L;

    private final int command;

    public ControlEventObject(int command) {
        this.command = command;
    }

    /*
     * (non-Javadoc)
     * 
     * @see java.lang.Object#toString()
     */
    @Override
    public String toString() {
        StringBuilder sb = new StringBuilder();
        sb.append(this.getClass().getName());
        sb.append("\n");

        sb.append("command=");
        sb.append(command);
        return sb.toString();
    }

    /*
     * (non-Javadoc)
     * 
     * @see java.lang.Object#hashCode()
     */
    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + command;
        return result;
    }

    /*
     * (non-Javadoc)
     * 
     * @see java.lang.Object#equals(java.lang.Object)
     */
    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        ControlEventObject other = (ControlEventObject) obj;
        if (command != other.command) {
            return false;
        }
        return true;
    }

}